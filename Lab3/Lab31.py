import pandas as pd
import json
import re


def clean_output(text):
    """
    Убираем markdown-разметку и лишние символы.
    """
    if text is None:
        return ""

    text = str(text)

    text = text.replace("```json", "")
    text = text.replace("```JSON", "")
    text = text.replace("```", "")

    return text.strip()


def extract_complete_json_objects(text):
    """
    Достаёт все завершённые JSON-объекты {...} из текста.
    Работает даже если общий список [...] оборван.
    """
    text = clean_output(text)

    objects = []
    stack = 0
    start = None

    for i, ch in enumerate(text):
        if ch == "{":
            if stack == 0:
                start = i
            stack += 1

        elif ch == "}":
            stack -= 1
            if stack == 0 and start is not None:
                obj_text = text[start:i + 1]
                objects.append(obj_text)
                start = None

    return objects


def parse_product_object(obj_text):
    """
    Парсит один объект продукта.
    Если json.loads не сработал, достаёт поля регулярками.
    """
    obj_text = obj_text.strip()


    try:
        obj = json.loads(obj_text)
        if isinstance(obj, dict):
            return obj
    except Exception:
        pass

    # 2. Исправляем частые проблемы
    fixed = obj_text
    fixed = fixed.replace("'", '"')
    fixed = fixed.replace("“", '"').replace("”", '"')
    fixed = fixed.replace("«", '"').replace("»", '"')
    fixed = re.sub(r",\s*}", "}", fixed)

    try:
        obj = json.loads(fixed)
        if isinstance(obj, dict):
            return obj
    except Exception:
        pass

    # 3. Fallback: достаём name/category/confidence регулярками
    name_match = re.search(r'"name"\s*:\s*"([^"]+)"', obj_text)
    category_match = re.search(r'"category"\s*:\s*"([^"]+)"', obj_text)
    confidence_match = re.search(r'"confidence"\s*:\s*([0-9.]+)', obj_text)

    if not name_match:
        return None

    name = name_match.group(1).strip()
    category = category_match.group(1).strip() if category_match else "другое"

    try:
        confidence = float(confidence_match.group(1)) if confidence_match else 1.0
    except Exception:
        confidence = 1.0

    return {
        "name": name,
        "category": category,
        "confidence": confidence
    }


def extract_products_from_raw_output(raw_output):
    """
    Главная функция: получает raw_output модели и возвращает список продуктов.
    """
    text = clean_output(raw_output)

    product_objects = extract_complete_json_objects(text)

    products = []

    for obj_text in product_objects:
        product = parse_product_object(obj_text)

        if product is None:
            continue

        name = str(product.get("name", "")).strip()

        if not name:
            continue

        category = str(product.get("category", "другое")).strip()

        try:
            confidence = float(product.get("confidence", 1.0))
        except Exception:
            confidence = 1.0

        products.append({
            "name": name,
            "category": category,
            "confidence": confidence
        })

    # Убираем дубли
    unique_products = []
    seen = set()

    for product in products:
        key = product["name"].lower()

        if key not in seen:
            unique_products.append(product)
            seen.add(key)

    return unique_products


def product_confidence(item, default=1.0):
    try:
        return float(item.get("confidence", default))
    except Exception:
        return default


def ingredient_names(det, min_confidence=0.0):
    """
    Возвращает названия продуктов из det.
    """
    products = det.get("products", [])

    names = []

    for item in products:
        if isinstance(item, dict):
            if product_confidence(item) < min_confidence:
                continue

            name = str(item.get("name", "")).strip()
            if name:
                names.append(name)
        elif isinstance(item, str):
            name = item.strip()
            if name:
                names.append(name)

    # Убираем дубли
    names = list(dict.fromkeys(names))

    return names


def products_to_eval_text(products, min_confidence=0.0):
    names = []

    for item in products:
        if isinstance(item, dict):
            if product_confidence(item) < min_confidence:
                continue

            name = str(item.get("name", "")).strip()
        else:
            name = str(item).strip()

        if name and name not in names:
            names.append(name)

    return ". ".join(names)


