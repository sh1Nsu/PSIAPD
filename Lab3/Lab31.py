def build_recipe_prompt(ingredients, servings=2, preferences=""):
    ingredients_text = ", ".join(ingredients) if ingredients else "ингредиенты не распознаны"

    pref_text = preferences.strip()
    if not pref_text:
        pref_text = "нет особых ограничений"

    return f"""
У тебя есть список продуктов, распознанных на фото холодильника или продуктовой полки:

{ingredients_text}

Пожелания пользователя: {pref_text}
Количество порций: {servings}

Составь 2–3 практичных рецепта на русском языке.

Требования:
0. Невыдумывай рецепты
1. Используй в основном продукты из списка.
2. Дополнительно можно использовать только базовые вещи: вода, соль, перец, растительное масло, базовые сухие специи.
3. МОжешь предлагать продукты, которых нет в списке. Если очень нужен дополнительный продукт, пометь его как "опционально".
4. Если список продуктов скудный, предложи максимально простые блюда.
5. Можешь предлагать продукты не указанные в списке.
6. Для каждого рецепта укажи:
   - название;
   - какие распознанные продукты используются;
   - базовые добавки;
   - время приготовления;
   - пошаговый способ;
   - короткую заметку, что можно заменить или убрать.


Формат ответа:

## 1. Название
**Используем:** ...
**Базовые добавки:** ...
**Время:** ...
**Шаги:**
1. ...
2. ...
**Замены:** ...

## 2. ...
"""


@torch.inference_mode()
def generate_recipes_from_products(ingredients, servings=2, preferences="", max_new_tokens=900):
    messages = [
        {
            "role": "system",
            "content": (
                "Ты аккуратный кулинарный ассистент. "
                "Ты не должен придумывать обязательные ингредиенты, которых нет в списке. "
                "Отвечай понятно, по-русски."
            )
        },
        {
            "role": "user",
            "content": build_recipe_prompt(ingredients, servings=servings, preferences=preferences)
        }
    ]

    text = llm_tokenizer.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=True
    )

    inputs = llm_tokenizer([text], return_tensors="pt").to(llm_model.device)

    output_ids = llm_model.generate(
        **inputs,
        max_new_tokens=max_new_tokens,
        do_sample=True,
        temperature=0.7,
        top_p=0.9,
        repetition_penalty=1.05,
    )

    generated_ids = output_ids[0][inputs.input_ids.shape[-1]:]
    answer = llm_tokenizer.decode(generated_ids, skip_special_tokens=True)
    return answer.strip()


def normalize_eval_text(text):
    text = "" if text is None else str(text).lower().replace("ё", "е")
    text = re.sub(r"\s+", " ", text)
    return text.strip()


def split_eval_items(text):
    if pd.isna(text):
        return []

    parts = re.split(r"[.;,\n]+", str(text))
    items = []

    for part in parts:
        item = normalize_eval_text(part).strip('"').strip("'").strip()
        if item and item not in items:
            items.append(item)

    return items


def score_recipe_ingredients(recipe_text, input_products, invented_required_products=""):
    recipe_norm = normalize_eval_text(recipe_text)

    input_products_norm = []
    for product in input_products:
        product_norm = normalize_eval_text(product)
        if product_norm and product_norm not in input_products_norm:
            input_products_norm.append(product_norm)

    used_products = [
        product
        for product in input_products_norm
        if product in recipe_norm
    ]

    not_used_products = [
        product
        for product in input_products_norm
        if product not in used_products
    ]

    invented_products = split_eval_items(invented_required_products)

    ingredient_recall = (
        len(used_products) / len(input_products_norm)
        if input_products_norm
        else 1.0
    )
    ingredient_precision = (
        len(used_products) / (len(used_products) + len(invented_products))
        if used_products or invented_products
        else 1.0
    )
    ingredient_f1 = (
        2 * ingredient_precision * ingredient_recall / (ingredient_precision + ingredient_recall)
        if ingredient_precision + ingredient_recall
        else 0.0
    )

    return {
        "used_input_products": ". ".join(used_products),
        "not_used_input_products": ". ".join(not_used_products),
        "invented_required_products": ". ".join(invented_products),
        "ingredient_precision": ingredient_precision,
        "ingredient_recall": ingredient_recall,
        "ingredient_f1": ingredient_f1,
        "num_input_products": len(input_products_norm),
        "num_used_input_products": len(used_products),
        "num_invented_required_products": len(invented_products),
    }

