# - короче продукты внутри ячейки разделяются точкой: .

import pandas as pd
import re


def split_products(text):
    """
    Делит продукты внутри одной ячейки по точке.
    Пример:
    'яблоки. апельсины. болгарский перец'
    ->
    {'яблоки', 'апельсины', 'болгарский перец'}
    """
    if pd.isna(text):
        return set()

    text = str(text).lower().replace("ё", "е")


    parts = re.split(r"[.;,\n]+", text)

    items = []

    for p in parts:
        p = p.strip()


        p = re.sub(r"\s+", " ", p)

        p = p.strip('"').strip("'").strip()

        if p:
            items.append(p)

    return set(items)


def score_row(predicted_text, true_text):
    pred = split_products(predicted_text)
    true = split_products(true_text)

    missed = sorted(true - pred)
    hallucinated = sorted(pred - true)
    correct = sorted(pred & true)

    precision = len(correct) / len(pred) if pred else (1.0 if not true else 0.0)
    recall = len(correct) / len(true) if true else (1.0 if not pred else 0.0)
    f1 = 2 * precision * recall / (precision + recall) if precision + recall else 0.0

    return {
        "correct": ". ".join(correct),
        "missed": ". ".join(missed),
        "hallucinated": ". ".join(hallucinated),
        "precision": precision,
        "recall": recall,
        "f1": f1,
        "num_predicted": len(pred),
        "num_true": len(true),
        "num_correct": len(correct),
        "num_missed": len(missed),
        "num_hallucinated": len(hallucinated),
    }


def build_score_df(source_df, predicted_col="predicted_products"):
    scores = []

    for _, row in source_df.iterrows():
        scores.append(
            score_row(
                row.get(predicted_col, ""),
                row.get("true_products", "")
            )
        )

    return pd.concat(
        [
            source_df.reset_index(drop=True),
            pd.DataFrame(scores).reset_index(drop=True)
        ],
        axis=1
    )



