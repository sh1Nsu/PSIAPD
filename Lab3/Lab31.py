def extract_json(text):
    """
    Пытаемся достать JSON из ответа модели.
    Если модель вернула текст вокруг JSON, вырезаем первый JSON-массив.
    """
    text = text.strip()

    try:
        return json.loads(text)
    except:
        pass

    match = re.search(r"\[.*\]", text, re.DOTALL)
    if match:
        try:
            return json.loads(match.group(0))
        except:
            pass

    return []


def move_inputs_to_model_device(inputs):
    moved = {}

    for key, value in inputs.items():
        value = value.to(vlm_model.device)

        if torch.cuda.is_available() and torch.is_floating_point(value):
            value = value.to(torch.float16)

        moved[key] = value

    return moved


def detect_products(image_path, max_new_tokens=512):
    prompt = """
Ты анализируешь фото холодильника или продуктовой полки.

Найди только реально видимые продукты питания.
Не выдумывай продукты.
Если продукт плохо виден, укажи низкую уверенность.

Верни ответ строго в JSON-формате без пояснений:

[
  {
    "name": "название продукта на русском",
    "category": "овощи / фрукты / молочные / мясо / рыба / напитки / крупы / соусы / другое",
    "confidence": 0.0
  }
]
"""

    messages = [
        {
            "role": "user",
            "content": [
                {"type": "image", "image": image_path},
                {"type": "text", "text": prompt},
            ],
        }
    ]

    text = vlm_processor.apply_chat_template(
        messages,
        tokenize=False,
        add_generation_prompt=True
    )

    image_inputs, video_inputs = process_vision_info(messages)

    inputs = vlm_processor(
        text=[text],
        images=image_inputs,
        videos=video_inputs,
        padding=True,
        return_tensors="pt",
    ).to(vlm_model.device)

    with torch.inference_mode():
        generated_ids = vlm_model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            use_cache=False,   # меньше расход памяти
        )

    generated_ids_trimmed = [
        out_ids[len(in_ids):]
        for in_ids, out_ids in zip(inputs.input_ids, generated_ids)
    ]

    output_text = vlm_processor.batch_decode(
        generated_ids_trimmed,
        skip_special_tokens=True,
        clean_up_tokenization_spaces=False,
    )[0]

    products = extract_json(output_text)

    # чистим память после каждого изображения
    del inputs, generated_ids, generated_ids_trimmed
    gc.collect()
    torch.cuda.empty_cache()

    return {
        "image_path": image_path,
        "raw_output": output_text,
        "products": products,
    }

